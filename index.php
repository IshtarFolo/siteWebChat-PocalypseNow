    <p class="mb-5">Ce projet explore un flux de travail complet de création 3D, de la modélisation à l’animation.<br><br>

		Le chat est d’abord sculpté dans <strong>ZBrush</strong>, en s’appuyant sur des références pour capturer son anatomie. Le modèle est ensuite exporté en <strong>FBX</strong> vers <strong>Maya</strong>, où ses <strong>UVs</strong> sont dépliées et des textures de base appliquées.<br><br>

		Après cela, l'armature du squelette est réalisée (<i>rigging</i>) et la structure squeletique est attaché et configurée (<i>skinning</i>) pour permettre l’animation. Une fois les mouvements testés, la chevelure/ pelage (<i>grooming</i>) est ajouté avec XGen pour un pelage le plus réaliste possible.<br><br>

		Enfin, le modèle est rendu dans <strong>Maya</strong>, finalisant ainsi un processus qui intègre <strong>modélisation</strong>, <strong>rigging</strong>, <strong>animation</strong> et <strong>grooming</strong> dans des logiciels professionnels.
